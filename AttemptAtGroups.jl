Linear Representations of Finite Groups by Jean-Pierre Serre

More generally, suppose that G acts on a finite set X. This means that,
for each s ∈ G, there is given a permutation x⥛sx of X, satisfying the identities
1x = x
s(tx) = (st)x if s,t ∈G, x ∈X

Let V be a vector space having a basis (eₓ) x∈X indexed by the elements of X.
For s ∈ G let ρ_s be the linear map of V into V which sends e_x to e_{sx};
the linear representation of G thus obtained is called the permutation
representation associated with X.
